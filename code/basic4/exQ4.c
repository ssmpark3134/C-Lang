#include <stdio.h>

int main() {
    int choice;

    do {
        // 1. 메뉴판 출력
        printf("\n======= 🍳 오늘 뭐 먹지? 요리 추천 🍳 =======\n");
        printf("1. 한식 (김치찌개와 불고기)\n");
        printf("2. 일식 (신선한 초밥과 라멘)\n");
        printf("3. 중식 (바삭한 탕수육과 짜장면)\n");
        printf("4. 양식 (꾸덕한 까르보나라와 스테이크)\n");
        printf("0. 프로그램 종료\n");
        printf("=============================================\n");
        printf("원하시는 요리 종류의 번호를 입력하세요: ");
        
        // 2. 사용자 입력 받기
        scanf("%d", &choice);

        // 3. 입력값에 따른 메뉴 출력
        printf("\n---------------------------------------------\n");
        switch (choice) {
            case 1:
                printf("👉 [한식]을 선택하셨습니다.\n");
                printf("보글보글 끓는 김치찌개와 달콤 짭조름한 불고기 조합을 추천합니다!\n");
                break;
            case 2:
                printf("👉 [일식]을 선택하셨습니다.\n");
                printf("입에서 녹는 활어 초밥과 진한 돈코츠 라멘은 어떠신가요?\n");
                break;
            case 3:
                printf("👉 [중식]을 선택하셨습니다.\n");
                printf("불맛 가득한 짜장면과 겉바속촉 탕수육이 딱이겠네요!\n");
                break;
            case 4:
                printf("👉 [양식]을 선택하셨습니다.\n");
                printf("고소한 크림 까르보나라 파스타와 미디엄 레어 스테이크를 즐겨보세요.\n");
                break;
            case 0:
                printf("👋 프로그램을 종료합니다. 맛있는 식사 되세요!\n");
                break;
            default:
                printf("❌ 잘못된 번호입니다. 0번부터 4번 사이의 번호를 입력해주세요.\n");
                break;
        }
        printf("---------------------------------------------\n");

    } while (choice != 0); // 4. 조건식: choice가 0이 아니면 계속 반복

    return 0;
}